class TestComposeAcmeDns:
    def test_acme_dns_present(self, sample_manifest):
        compose = yaml.safe_load(mclib.generate_compose(sample_manifest))
        acme = compose['services']['acme-dns']
        assert acme['build'] == './acme-dns'
        assert acme['container_name'] == 'acme-dns'
        assert acme['restart'] == 'unless-stopped'

    def test_acme_dns_ports(self, sample_manifest):
        compose = yaml.safe_load(mclib.generate_compose(sample_manifest))
        ports = compose['services']['acme-dns']['ports']
        assert '53:53' in ports
        assert '53:53/udp' in ports
        assert '127.0.0.1:8053:80' in ports

    def test_acme_dns_volumes(self, sample_manifest):
        compose = yaml.safe_load(mclib.generate_compose(sample_manifest))
        volumes = compose['services']['acme-dns']['volumes']
        assert './acme-dns/config:/etc/acme-dns:ro' in volumes
        assert './acme-dns/data:/var/lib/acme-dns' in volumes


