"""Tests for docker-compose and nginx config generation."""

import yaml
import mclib


# ---------------------------------------------------------------------------
# Compose generation — structure
# ---------------------------------------------------------------------------

class TestComposeStructure:
    def test_has_services_and_networks(self, sample_manifest):
        compose = yaml.safe_load(mclib.generate_compose(sample_manifest))
        assert 'services' in compose
        assert 'networks' in compose
        assert 'minecraft-net' in compose['networks']

    def test_has_header_comment(self, sample_manifest):
        raw = mclib.generate_compose(sample_manifest)
        assert raw.startswith('# Generated by mc-generate')

    def test_service_order(self, sample_manifest):
        raw = mclib.generate_compose(sample_manifest)
        acme_pos = raw.index('acme-dns')
        router_pos = raw.index('mc-router')
        creative_pos = raw.index('creative:')
        test_pos = raw.index('test:')
        assert acme_pos < router_pos < creative_pos < test_pos


# ---------------------------------------------------------------------------
# Compose generation — acme-dns (static service)
