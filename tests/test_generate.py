# Compose generation — MC servers
# ---------------------------------------------------------------------------

class TestComposeMcServer:
    def test_basic_fields(self, sample_manifest):
        compose = yaml.safe_load(mclib.generate_compose(sample_manifest))
        creative = compose['services']['creative']
        assert creative['image'] == 'itzg/minecraft-server'
        assert creative['container_name'] == 'creative'
        assert creative['restart'] == 'unless-stopped'
        assert 'minecraft-net' in creative['networks']

    def test_env_file(self, sample_manifest):
        compose = yaml.safe_load(mclib.generate_compose(sample_manifest))
        assert compose['services']['creative']['env_file'] == ['servers/creative/env']
        assert compose['services']['test']['env_file'] == ['servers/test/env']

    def test_environment_constants(self, sample_manifest):
        compose = yaml.safe_load(mclib.generate_compose(sample_manifest))
        env = compose['services']['creative']['environment']
        assert env['EULA'] == 'TRUE'
        assert env['WHITELIST_ENABLED'] == 'true'
        assert env['ENFORCE_WHITELIST'] == 'true'
        assert env['RCON_PASSWORD'] == '${RCON_PASSWORD:-changeme}'

    def test_environment_from_manifest(self, sample_manifest):
        compose = yaml.safe_load(mclib.generate_compose(sample_manifest))
        env = compose['services']['creative']['environment']
        assert env['TYPE'] == 'FABRIC'
        assert env['VERSION'] == 'LATEST'
        assert env['MEMORY'] == '4G'
        assert env['MODE'] == 'creative'
        assert env['MOTD'] == 'Creative Server'

    def test_modrinth_projects(self, sample_manifest):
        compose = yaml.safe_load(mclib.generate_compose(sample_manifest))
        projects = compose['services']['creative']['environment']['MODRINTH_PROJECTS']
        lines = [l.strip() for l in projects.strip().split('\n')]
        # fabric-base group + modrinth_mods
        assert 'fabric-api' in lines
        assert 'lithium' in lines
        assert 'noisiumforked' in lines
        assert 'distanthorizons' in lines
        assert 'bluemap' in lines
        assert 'simple-voice-chat' in lines

    def test_modrinth_version_type(self, sample_manifest):
        compose = yaml.safe_load(mclib.generate_compose(sample_manifest))
        # creative has modrinth_version_type: beta
        creative_env = compose['services']['creative']['environment']
        assert creative_env['MODRINTH_ALLOWED_VERSION_TYPE'] == 'beta'
        # test uses default (release), so key should be absent
        test_env = compose['services']['test']['environment']
        assert 'MODRINTH_ALLOWED_VERSION_TYPE' not in test_env

    def test_players(self, sample_manifest):
        compose = yaml.safe_load(mclib.generate_compose(sample_manifest))
        env = compose['services']['creative']['environment']
        assert env['OPS'] == 'd2683803'
        assert env['WHITELIST'] == 'd2683803,Kam93'

    def test_seed_quoted(self, sample_manifest):
        compose = yaml.safe_load(mclib.generate_compose(sample_manifest))
        test_env = compose['services']['test']['environment']
        assert test_env['SEED'] == '-4172144997902289642'
        # creative has no seed
        creative_env = compose['services']['creative']['environment']
        assert 'SEED' not in creative_env

    def test_seed_quoted_in_raw_yaml(self, sample_manifest):
        """Negative seeds must be quoted in YAML to avoid being parsed as numbers."""
        raw = mclib.generate_compose(sample_manifest)
        assert '"-4172144997902289642"' in raw

    def test_svc_port(self, sample_manifest):
        compose = yaml.safe_load(mclib.generate_compose(sample_manifest))
        creative_ports = compose['services']['creative']['ports']
        assert '24454:24454/udp' in creative_ports
        # test has svc: false
        test_ports = compose['services']['test']['ports']
        assert '24454:24454/udp' not in test_ports

    def test_bluemap_port(self, sample_manifest):
        compose = yaml.safe_load(mclib.generate_compose(sample_manifest))
        creative_ports = compose['services']['creative']['ports']
        assert '127.0.0.1:8100:8100' in creative_ports
        test_ports = compose['services']['test']['ports']
        assert '127.0.0.1:8101:8100' in test_ports

    def test_data_volume(self, sample_manifest):
        compose = yaml.safe_load(mclib.generate_compose(sample_manifest))
        assert './servers/creative/data:/data' in compose['services']['creative']['volumes']

    def test_resource_limits(self, sample_manifest):
        compose = yaml.safe_load(mclib.generate_compose(sample_manifest))
        creative_limits = compose['services']['creative']['deploy']['resources']['limits']
        assert creative_limits['memory'] == '5g'
        assert creative_limits['cpus'] == '2.0'  # permanent
        test_limits = compose['services']['test']['deploy']['resources']['limits']
        assert test_limits['memory'] == '3g'
        assert test_limits['cpus'] == '1.0'  # ephemeral

    def test_jar_mods_volume_and_env(self):
        """Servers with jar_mods get shared/mods volume and MODS env var."""
        manifest = {
            'players': {'ops': ['d2683803'], 'whitelist': ['d2683803']},
            'mod_groups': {},
            'servers': {
                'modded': {
                    'type': 'FABRIC',
                    'version': 'LATEST',
                    'mode': 'creative',
                    'memory': '2G',
                    'tier': 'ephemeral',
                    'mod_groups': [],
                    'modrinth_mods': [],
                    'jar_mods': ['custom-mod.jar', 'dev-build.jar'],
                    'svc': False,
                    'seed': None,
                    'motd': 'Modded Server',
                },
            },
        }
        compose = yaml.safe_load(mclib.generate_compose(manifest))
        modded = compose['services']['modded']
        assert './shared/mods:/shared-mods:ro' in modded['volumes']
        env = modded['environment']
        assert env['MODS'] == '/shared-mods/custom-mod.jar,/shared-mods/dev-build.jar'

    def test_no_ports_key_when_empty(self):
        """Server with no bluemap and no svc shouldn't have a ports key."""
        manifest = {
            'players': {'ops': ['d2683803'], 'whitelist': ['d2683803']},
            'mod_groups': {},
            'servers': {
                'vanilla': {
                    'type': 'VANILLA',
                    'version': 'LATEST',
                    'mode': 'survival',
                    'memory': '2G',
                    'tier': 'ephemeral',
                    'mod_groups': [],
                    'modrinth_mods': [],
                    'jar_mods': [],
                    'svc': False,
                    'bluemap': False,
                    'seed': None,
                    'motd': 'Vanilla Server',
                },
            },
        }
        compose = yaml.safe_load(mclib.generate_compose(manifest))
        assert 'ports' not in compose['services']['vanilla']

    def test_bluemap_autodetected_from_mods(self):
        """When bluemap field not set, infer from resolved mods."""
        manifest = {
            'players': {'ops': ['d2683803'], 'whitelist': ['d2683803']},
            'mod_groups': {},
            'servers': {
                'auto': {
                    'type': 'FABRIC',
                    'version': 'LATEST',
                    'mode': 'creative',
                    'memory': '2G',
                    'tier': 'ephemeral',
                    'mod_groups': [],
                    'modrinth_mods': ['bluemap'],
                    'jar_mods': [],
                    'svc': False,
                    'seed': None,
                    'motd': 'Auto Server',
                },
            },
        }
        compose = yaml.safe_load(mclib.generate_compose(manifest))
        ports = compose['services']['auto']['ports']
        # Should auto-detect bluemap and assign port
        assert any('8100' in str(p) for p in ports)


