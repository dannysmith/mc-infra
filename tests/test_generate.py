class TestNginxGeneration:
    def test_has_header_comment(self, sample_manifest):
        raw = mclib.generate_nginx(sample_manifest)
        assert raw.startswith('# Generated by mc-generate')

    def test_http_redirect(self, sample_manifest):
        raw = mclib.generate_nginx(sample_manifest)
        assert 'listen 80;' in raw
        assert 'return 301 https://$host$request_uri;' in raw

    def test_creative_server_block(self, sample_manifest):
        raw = mclib.generate_nginx(sample_manifest)
        assert 'server_name map-creative.mc.danny.is;' in raw
        assert 'proxy_pass http://127.0.0.1:8100;' in raw

    def test_test_server_block(self, sample_manifest):
        raw = mclib.generate_nginx(sample_manifest)
        assert 'server_name map-test.mc.danny.is;' in raw
        assert 'proxy_pass http://127.0.0.1:8101;' in raw

    def test_ssl_include(self, sample_manifest):
        raw = mclib.generate_nginx(sample_manifest)
        assert 'include /opt/minecraft/nginx/conf.d/ssl.conf;' in raw

    def test_websocket_support(self, sample_manifest):
        raw = mclib.generate_nginx(sample_manifest)
        assert 'proxy_set_header Upgrade $http_upgrade;' in raw
        assert 'proxy_set_header Connection "upgrade";' in raw

    def test_no_block_for_non_bluemap_server(self):
        manifest = {
            'players': {'ops': [], 'whitelist': []},
            'mod_groups': {},
            'servers': {
                'vanilla': {
                    'type': 'VANILLA',
                    'mode': 'survival',
                    'memory': '2G',
                    'tier': 'ephemeral',
                    'bluemap': False,
                    'modrinth_mods': [],
                    'mod_groups': [],
                    'jar_mods': [],
                    'svc': False,
                },
            },
        }
        raw = mclib.generate_nginx(manifest)
        assert 'map-vanilla' not in raw

    def test_only_bluemap_servers_get_blocks(self):
        manifest = {
            'players': {'ops': [], 'whitelist': []},
            'mod_groups': {},
            'servers': {
                'mapped': {
                    'type': 'FABRIC',
                    'mode': 'creative',
                    'memory': '2G',
                    'tier': 'ephemeral',
                    'bluemap': True,
                    'bluemap_port': 8100,
                    'modrinth_mods': ['bluemap'],
                    'mod_groups': [],
                    'jar_mods': [],
                    'svc': False,
                },
                'unmapped': {
                    'type': 'VANILLA',
                    'mode': 'survival',
                    'memory': '2G',
                    'tier': 'ephemeral',
                    'bluemap': False,
                    'modrinth_mods': [],
                    'mod_groups': [],
                    'jar_mods': [],
                    'svc': False,
                },
            },
        }
        raw = mclib.generate_nginx(manifest)
        assert 'map-mapped.mc.danny.is' in raw
        assert 'map-unmapped' not in raw
